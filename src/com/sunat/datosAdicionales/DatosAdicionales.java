package com.sunat.datosAdicionales;

public class DatosAdicionales {

    public String guia;
    public String tiempo;
    public String bultos;
    public String fca;
    public String sms;
    public String observacion;
    public String cantidadTotal;
    public String cantidadCajas;

    public void setGuia(String guia) {
        this.guia = guia;
    }

    public void setTiempo(String tiempo) {
        this.tiempo = tiempo;
    }

    public void setBultos(String bultos) {
        this.bultos = bultos;
    }

    public void setFca(String fca) {
        this.fca = fca;
    }

    public void setSms(String sms) {
        this.sms = sms;
    }
    
    public void setObservacion(String observacion ){
     this.observacion = observacion;
    }
    
    public void setCantidadTotal(String cantidadTotal ){
     this.cantidadTotal = cantidadTotal;
    }
    
    public void setCantidadCajas(String cantidadCajas){
     this.cantidadCajas = cantidadCajas;
    
    }
    
    
    
}
